        /// <returns></returns>
        public static Int64 ObjToLong(this object thisValue)
        {
            Int64 reval = 0;
            if (thisValue == null) return 0;
            if (thisValue != null && thisValue != DBNull.Value && Int64.TryParse(thisValue.ToString(), out reval))
